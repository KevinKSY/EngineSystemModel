static void sf_gateway_c3_EngineSystemSubmodel
  (SFc3_EngineSystemSubmodelInstanceStruct *chartInstance)
{
  real_T c3_omega_nom;
  _sfTime_ = sf_get_time(chartInstance->S);
  c3_omega_nom = *chartInstance->c3_omegaE / chartInstance->c3_omegaEMax;
  *chartInstance->c3_effMech = 1.0 - (chartInstance->c3_coeffMechEff[1] *
    c3_omega_nom - chartInstance->c3_coeffMechEff[0] / (1.0 -
    chartInstance->c3_coeffMechEff[1]) * (c3_omega_nom -
    chartInstance->c3_coeffMechEff[1]) * c3_omega_nom);
}

