  sf_mex_assign(&c3_y, sf_mex_createcellmatrix(2, 1), false);
  c3_u = *chartInstance->c3_effMech;
  c3_b_y = NULL;
  sf_mex_assign(&c3_b_y, sf_mex_create("y", &c3_u, 0, 0U, 0U, 0U, 0), false);
